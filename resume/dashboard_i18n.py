def get_dashboard_strings(lang: str) -> dict[str, str]:
    code = (lang or "uz")[:2]
    primary = _STR.get(code) or _STR["uz"]
    fallback_en = _STR["en"]

    def pick(key: str) -> str:
        return primary.get(key) or fallback_en.get(key, key)

    return {k: pick(k) for k in fallback_en}


def dash_key(strings: dict[str, str], key: str) -> str:
    return strings.get(key, key)
